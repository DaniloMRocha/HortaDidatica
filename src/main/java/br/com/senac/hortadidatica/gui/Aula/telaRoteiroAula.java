    private void btnVerRoteiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerRoteiroActionPerformed
        // TODO add your handling code here:
        String posicaoTabelaAula = getPosicaoTabelaAula();

    if (!posicaoTabelaAula.equals("-1")) {
        DadoAula aulaSelecionada = DAO.obterPorId(DadoAula.class, posicaoTabelaAula);

        telaVerRoteiroAula TelaVerRoteiroAula = new telaVerRoteiroAula();

        TelaVerRoteiroAula.txtVerNomeAula.setText(aulaSelecionada.getTituloAula());
        TelaVerRoteiroAula.txtVerTempoAula.setText(aulaSelecionada.getTempoAula());
        TelaVerRoteiroAula.txtVerMaterialAula.setText(aulaSelecionada.getMaterialAula());
        TelaVerRoteiroAula.txtVerObjetivoAula.setText(aulaSelecionada.getObjetivoAula());
        TelaVerRoteiroAula.txtVerProcedimentoAula.setText(aulaSelecionada.getProcedimentoAula());
        TelaVerRoteiroAula.txtVerAvaliacaoAula.setText(aulaSelecionada.getAvaliacaoAula());

        TelaVerRoteiroAula.setVisible(true);
        
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma aula!");
    }  
    }//GEN-LAST:event_btnVerRoteiroActionPerformed

