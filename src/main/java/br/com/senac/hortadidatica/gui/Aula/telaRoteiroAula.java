public static String getPosicaoTabelaAula() {
    int rowIndex = tblRoteiroAula.getSelectedRow();

    if (rowIndex == -1) {
        return "-1";
    } else {
        return String.valueOf(tblRoteiroAula.getValueAt(rowIndex, 0));
    }
}

public static void iniciaTabelaAula(){
    try{
        DAO aulaDao = new DAO();
        List<DadoAula> listaAula = aulaDao.listar(DadoAula.class);
        preencheTabelaAula(listaAula);
    }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu uma falha:\n" + e.getMessage());
    }
}

public static void preencheTabelaAula(List<DadoAula> listaAula){
    String columns[] = {"Id", "Nome do Roteiro"};
    String dados[][] = new String[listaAula.size()][columns.length];
    int i=0;
    for(DadoAula d: listaAula){
        dados[i] = new String[]{
            String.valueOf(d.getId()),
            d.getTituloAula()};
        i++;
        }
    DefaultTableModel tabelaAula = new DefaultTableModel(dados, columns);
    tblRoteiroAula.setModel(tabelaAula);
    TableColumnModel columnModel = tblRoteiroAula.getColumnModel();//essa linha e as próximas escondem a coluna id para a tabela ficar visualmente melhor
    columnModel.getColumn(0).setMinWidth(0);
    columnModel.getColumn(0).setMaxWidth(0);
    columnModel.getColumn(0).setWidth(0);
    }

