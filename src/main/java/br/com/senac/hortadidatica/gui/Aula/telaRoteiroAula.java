    private void btnExcluirRoteiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirRoteiroActionPerformed
        // TODO add your handling code here:
       if(!getPosicaoTabelaAula().equals("-1")){
            
            String[] options = { "Sim", "Não" };
            
            int deletar = JOptionPane.showOptionDialog(
                 rootPane,
                 "Tem certeza que deseja excluir as informações dessa aula?\n(essa ação não pode ser desfeita)", 
                 "Exclusão de aula", 
                 JOptionPane.DEFAULT_OPTION, 
                 JOptionPane.QUESTION_MESSAGE, 
                 null, 
                 options, 
                 options[0]
              
            );
            
            if (deletar == 0){
                DAO.excluir(DadoAula.class,  getPosicaoTabelaAula());
                iniciaTabelaAula();
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma aula!");
        }
    }//GEN-LAST:event_btnExcluirRoteiroActionPerformed

