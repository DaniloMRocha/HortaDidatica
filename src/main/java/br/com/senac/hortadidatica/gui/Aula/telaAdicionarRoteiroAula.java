    private void btnCadastrarAulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAulaActionPerformed
        // TODO add your handling code here:
        if(!campoVazioAula()){
            
            DadoAula aula = new DadoAula();
            aula.setTituloAula(txtAulaTitulo.getText());
            aula.setTempoAula(txtAulaTempo.getText());
            aula.setMaterialAula(txtAulaMaterial.getText());
            aula.setObjetivoAula(txtAulaObjetivo.getText());
            aula.setProcedimentoAula(txtAulaProcedimento.getText());
            aula.setAvaliacaoAula(txtAulaAvaliacao.getText());
            
            try{
                DAO aulaDao = new DAO();
                aulaDao.cadastrar(aula);
                JOptionPane.showMessageDialog(rootPane, "Aula cadastrada!");
                telaRoteiroAula.iniciaTabelaAula();

            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Ocorreu um erro ao cadastrar a aula:\n" + e.getMessage());
            }
        } 

    }//GEN-LAST:event_btnCadastrarAulaActionPerformed

