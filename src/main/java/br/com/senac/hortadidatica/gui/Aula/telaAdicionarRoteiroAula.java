private boolean campoVazioAula(){

    boolean empty = true; 

    if(txtAulaTitulo.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O título da aula deve ser preenchido!");    
    } else if(txtAulaTempo.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar o tempo de aula!");   
    } else if(txtAulaMaterial.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar os materiais usados na aula (plantas, pinças, microscópio etc.");     
    } else if(txtAulaObjetivo.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar os objetivos (geral e específicos) da aula");     
    } else if(txtAulaProcedimento.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar os procedimentos da aula");     
    }else if(txtAulaAvaliacao.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar o método de avaliação da aula");     
    }else{
         empty = false;
    }
     return empty;    
    }

