private boolean campoVazioItem(){

    boolean empty = true; 

    if(txtNomeItem.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Nome do item não pode estar vazio.");    
    } else if(txtQuantidade.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar a quantidade do item!");   
    } else if(txtPreco.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Informar o preço do item!");   
    } else{
         empty = false;
    }
     return empty;    
    }

public static String getPosicaoTabelaItem() {
    int rowIndex = tblItem.getSelectedRow();

    if (rowIndex == -1) {
        return "-1";
    } else {
        return String.valueOf(tblItem.getValueAt(rowIndex, 0));
    }
}

public static void iniciaTabelaItem(){
    try{
        DAO itemDao = new DAO();
        List<DadoItem> listaItem = itemDao.listar(DadoItem.class);
        preencheTabelaItem(listaItem);
    }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu uma falha:\n" + e.getMessage());
    }
}

public static void preencheTabelaItem(List<DadoItem> listaItem){
    String columns[] = {"Id", "Nome", "Quantidade", "Preço", "Preço total"};
    String dados[][] = new String[listaItem.size()][columns.length];
    int i=0;
    for(DadoItem d: listaItem){
        dados[i] = new String[]{
            String.valueOf(d.getId()),
            d.getNomeItem(),
            String.valueOf(d.getQuantidadeItem()),
            String.valueOf(d.getPrecoItem()),
            String.valueOf(d.getPrecoItemTotal())};
        i++;
        }
    DefaultTableModel tabelaItem = new DefaultTableModel(dados, columns);
    tblItem.setModel(tabelaItem);
    TableColumnModel columnModel = tblItem.getColumnModel();//essa linha e as próximas escondem a coluna id para a tabela ficar visualmente melhor
    columnModel.getColumn(0).setMinWidth(0);
    columnModel.getColumn(0).setMaxWidth(0);
    columnModel.getColumn(0).setWidth(0);
    }

