    private void btnAlterarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarItemActionPerformed
        // TODO add your handling code here:
        String posicaoTabelaItem = getPosicaoTabelaItem();

    if (!posicaoTabelaItem.equals("-1")) {
        DadoItem itemSelecionado = DAO.obterPorId(DadoItem.class, posicaoTabelaItem);

        telaAlterarItem TelaAlterarItem = new telaAlterarItem();

        TelaAlterarItem.txtAlterarItemNome.setText(itemSelecionado.getNomeItem());
        TelaAlterarItem.txtAlterarQuantidade.setText(String.valueOf(itemSelecionado.getQuantidadeItem()));
        TelaAlterarItem.txtAlterarPreco.setText(String.valueOf(itemSelecionado.getPrecoItem()));

        TelaAlterarItem.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma planta!");
    } 
    }//GEN-LAST:event_btnAlterarItemActionPerformed

